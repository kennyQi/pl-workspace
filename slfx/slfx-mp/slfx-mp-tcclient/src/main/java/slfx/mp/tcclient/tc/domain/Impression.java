package slfx.mp.tcclient.tc.domain;
/**
 * 游客印象
 * @author zhangqy
 *
 */
public class Impression {
	/**
	 * 游客印象Id
	 */
	private Integer impressionId;
	/**
	 * 游客印象名称
	 */
	private String impressionName;
	public Integer getImpressionId() {
		return impressionId;
	}
	public void setImpressionId(Integer impressionId) {
		this.impressionId = impressionId;
	}
	public String getImpressionName() {
		return impressionName;
	}
	public void setImpressionName(String impressionName) {
		this.impressionName = impressionName;
	}
	
	
}
