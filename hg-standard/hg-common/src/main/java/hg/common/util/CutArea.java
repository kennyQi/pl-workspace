package hg.common.util;

public class CutArea {

	/**
	 * 起始位置横坐标
	 */
	private int x;

	/**
	 * 起始位置纵坐标
	 */
	private int y;

	/**
	 * 剪裁宽度
	 */
	private int width;

	/**
	 * 剪裁高度
	 */
	private int height;

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}

}
